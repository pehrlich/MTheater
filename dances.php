        In 1953 and after reaching acclaim as a performing artist, Daw Oba Thaung (age 55), was appointed the first
        Instructor of Dance at the (then) State School of Music and Drama in Mandalay. She found herself at a dead end
        for lack of a set syllabus in classical dance. <br>
        <br>
        She was keenly aware of the diluting influences in the contemporary performances due to the inclusion of
        elements from neighboring countries and modern media. She saw this as a threat to the national genius of the art
        form. Therefore she took on the task of capturing a kernel of genuine Myanmar dance art. <br>
        <br>
        The result is a formulation of five dance courses intended as a five-year term of study. Each of the five
        courses is broken into dance sequences comprising a total of 125 stages, each stage of precisely ten minutes.
        <br>
        The first course is dance accompanied solely by rhythmic beating. The &#147;kabya loot aka&#148;
        (dance-without-verse) is synchronized to the beats of four drums (&#147;tub-bado-bay&#148;, &#147;jay-jay&#148;,
        &#147;tub-jay&#148;, and &#147;tups-jay&#148;) and clapper. The dancer must coordinate movements of body, legs
        and hands. As you hear it performed, you will find it catchy and may feel like taking part. You will find it
        good as a fitness exercise (although Daw Oba Thaung might have some corrections on your style.) <br>
