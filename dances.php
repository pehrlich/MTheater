        Classical cane ball is probably the national sport of Myanmar. The game can be traced at least to the Pyu
        kingdoms of lower Myanmar. A silver cane ball, excavated from Thayekhittaya (now Pyay), was dated by
        archaeologists to 500-700 AD. The sport is now played in many countries in Southeast Asia, including Malaysia,
        Indonesia, Laos, Vietnam, Cambodia and Thailand. However the style of play differs according to nationality and
        in most cases, excluding Myanmar, has been transformed nearly completely into a competition between two teams.
        <br>
        <br>
        Cane ball is popular at all levels of play for fitness and entertainment, and most males in Myanmar learn to
        play to some degree. People play both as a solo exercise and in groups (usually six players). Styles of
        individual and team play are highly stylized with dramatic postures and artistic ball handling. <br>
        <br>
        Intentional touching of the ball with hands or elbows is disallowed. This leaves kicking, toeing, or
        decelerating the ball with the foot (inside-, or outer-edge, instep, toe, sole, heel, ankle) knee, head, back,
        shoulder, and any other appendage. <br>
        <br>
        Cane ball competitions are an important part of the Thingyan (New Year) festival but expert players are
        generally seen entertaining the crowd in the early evening at many other festivals.<br>
