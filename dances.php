            <b>Apprenticeship in Myanmar Dance</b><br>
            To achieve star status, a classical Myanmar dancer needs more than dance skills and mastery of a repertory.
            He must have a fine vocal ability, a handsome appearance, and be of medium build, neither too fat nor too
            thin. To add to this, he must have good dramatic skills and be able to convey many different roles through
            gesture and facial expression. Finally he must have eloquence and ability to connect to an audience in
            speech. <br>
            <br>
            A student can undergo training by a coach and usually can become a skillful dancer in a relatively short
            period of time. But he will still fall short in singing and dramatic roles. It also takes much longer to
            learn the classic songs by heart and to master the art of composing original music and lyrics. <br>
            <br>
            The Apprentice Dancer has reached the intermediate stage in this training. Most generally, he or she is
            given opportunities to perform between acts with a professional troupe or to take occasional roles as a
